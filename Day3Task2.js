let grade = 100;

if ( 0 <= grade > 50) {
	console.log("Failed");
} 

else if (50 <= grade > 65) {
	console.log("Accepted");
}
else if (65 <= grade > 75 ) {
	console.log("Good");
} 
else if (75 <= grade > 85  ) {
	console.log("Very Good");
} 
else if (85 <= grade >= 100) {
	console.log("Excellent");
}